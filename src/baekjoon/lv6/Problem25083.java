/**
 * [문제 맥락]
 * - 큰따옴표(")와 역슬래시(\) 등 예약 문자를 이스케이프 시퀸스로 처리하는 문제
 * - java 15 이후부터는 Text Block(""") 문법으로 간단히 처리 가능
 */
public class Problem25083 {
    public static void main(String[] args) {
        System.out.println("         ,r'\"7");
        System.out.println("r`-_   ,'  ,/");
        System.out.println(" \\. \". L_r'");
        System.out.println("   `~\\/");
        System.out.println("      |");
        System.out.println("      |");
    }
}
